 # Pedir dos números al usuario
numero1 = float(input("Ingresar el primer número:"))
numero2 = float(input("Ingresar el segundo número:"))
#Calcular la suma de los dos números 
suma = numero1 + numero2
#Calcular la raiz cuadrada de la suma
raiz_cuadrada = suma **0.5
#Mostramos el resultado
print(f"La raiz cuadrada de la suma es:{raiz_cuadrada}")